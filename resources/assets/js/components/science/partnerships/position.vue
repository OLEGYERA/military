                            <p style="text-align: center;">ВОЄННО-НАУКОВОГО ТОВАРИСТВА КВП НАУ</p>
                            <p style="text-align: center;">
                            </p><p style="text-align: left;">4.1. Структура ВНТ та організаційний механізм його діяльності визначаються Положенням, яке ухвалюється загальними зборами ВНТ та затверджується начальником кафедри.</p>
                            <p style="text-align: left;">
                            </p><p style="text-align: left;">4.2. Центром наукової роботи членів воєнно-наукового товариства кафедри є предметно-методичної комісія (далі – ПМК), де створюється секція, розробляється тематика наукової роботи студентів та курсантів за відповідним профілем.</p>
                            <p style="text-align: left;">
                            </p><p style="text-align: left;">4.3. Для безпосереднього наукового керівництва роботою секції обирається керівник наукового гуртка з числа науково-педагогічних і педагогічних працівників кафедри.</p>
                            <p style="text-align: left;">
                            </p><p style="text-align: left;">4.4. У секції студенти та курсанти закріплюються за науковими керівниками з числа науково-педагогічних працівників, які безпосередньо керують науковою роботою членів товариства.</p>
                            <p style="text-align: left;">
                            </p><p style="text-align: left;">4.5. На першому засіданні секції обирається голова та секретар з числа наукових керівників терміном на один рік.</p>
                            <p style="text-align: left;">
                            </p><p style="text-align: left;">4.6. На секретаря секції покладаються такі функції:</p>
                            <ul style="text-align: left;">
                                <li>розробка спільно з головою тематичного плану роботи секції на навчальний рік;</li>
                                <li>організація подання членами секції рефератів і наукових робіт на конкурс кафедри, університету, конкурси наукових робіт у межах Міністерства оборони України;</li>
                                <li>здійснення контролю за роботою членів секції;</li>
                                <li>планування і забезпечення підготовки доповідей і виступів членів секції на наукових конференціях і семінарах, засіданнях секції;</li>
                                <li>своєчасне інформування членів секції про наукові заходи, які проводяться на ПМК, кафедрі, університеті;</li>
                                <li>організація підготовки звітно-виборних зборів секції;</li>
                                <li>ведення журналу наукової секції.</li>
                            </ul>
                            <p style="text-align: left;">
                            </p><p style="text-align: left;">4.7. Вищим органом воєнно-наукового товариства КВП НАУ є загальні збори членів товариства, які обирають раду товариства терміном на один рік.</p>
                            <p style="text-align: left;">
                            </p><p style="text-align: left;">4.8. Усі питання на загальних зборах вирішуються більшістю голосів шляхом відкритого голосування.</p>
                            <p style="text-align: left;">
                            </p><p style="text-align: left;">4.9. Рада воєнно-наукового товариства обирається у складі голови, заступників, секретаря і членів ради. Число членів ради визначається на загальних зборах.</p>
                            <p style="text-align: left;">
                            </p><p style="text-align: left;">4.10. Рада воєнно-наукового товариства КВП НАУ:</p>
                            <ul style="text-align: center;">
                                <li style="text-align: left;">планує роботу воєнно-наукового товариства КВП НАУ на навчальний рік і розробляє тематичний план;</li>
                                <li style="text-align: left;">здійснює керівництво і контроль за роботою членів воєнно-наукового товариства в секціях та надає їм практичну допомогу;</li>
                            </ul>
