                            <p style="text-align: center;">Рис. Структура ВНТ КВП НАУ</p>
                            <p style="text-align: center;">
                            </p><p style="text-align: center;">
                        </p><ul style="text-align: center;">
                            <li style="text-align: left;">організовує повідомлення, лекції і доповіді членів воєнно-наукового товариства за новітніми досягненнями науки і техніки;</li>
                            <li style="text-align: left;">організує конкурси на кращу наукову роботу на кафедрі;</li>
                            <li style="text-align: left;">узагальнює і популяризує досвід роботи секцій;</li>
                            <li style="text-align: left;">організує подання членами воєнно-наукового товариства рефератів і наукових робіт на конкурс університету, конкурси наукових робіт, що проводиться у межах Міністерства оборони України;</li>
                            <li style="text-align: left;">організує і оформляє виставки кращих робіт, стенди і вітрини, які відображають досягнення наукової роботи членів воєнно-наукового товариства;</li>
                            <li style="text-align: left;">узагальнює і популяризує досвід роботи наукових секцій на ПМК;</li>
                            <li style="text-align: left;">подає командуванню кафедри пропозиції про заохочення членів воєнно-наукового товариства;</li>
                            <li style="text-align: left;">організує підготовку звітно-виборних зборів воєнно-наукового товариства кафедри.</li>
                        </ul>
                            <p style="text-align: left;">
                            </p><p style="text-align: left;">4.11. Тематичний план роботи ВНТ, загальний список членів ВНТ обговорюється на загальних зборах і затверджується начальником КВП НАУ.</p>
                            <p style="text-align: left;">
                            </p><p style="text-align: left;">4.12. Обов’язковою умовою членства в ВНТ кафедри для студентів та курсантів є успішне оволодіння навчальною програмою .</p>
                            <p style="text-align: left;">
                            </p><p style="text-align: left;">4.13. Прийом в члени товариства здійснюється на основі усної заяви.</p>
                            <p style="text-align: left;">
                            </p><p style="text-align: left;">4.14. Робота за темами наукових досліджень проводиться в дні та години, встановлені розпорядком дня КВП НАУ.</p>
                            <p style="text-align: left;">
                            </p><p style="text-align: left;">4.16. Перелік основних документів з планування та обліку роботи воєнно-наукового товариства КВП НАУ:</p>
                            <p style="text-align: left;">на кафедрі:</p>
                            <ul style="text-align: left;">
                                <li>тематичний план роботи воєнно-наукового товариства кафедри</li>
                                <li>звіт про підсумки роботи ВНТ за навчальний рік</li>
                                <li>список членів ВНТ кафедри.</li>
                                <li>протоколи засідань загальних зборів ВНТ кафедри</li>
                            </ul>
                            <p style="text-align: left;">в ПМК:</p>
                            <ul style="text-align: left;">
                                <li>тематичний план роботи секції воєнно-наукового товариства</li>
                                <li>звіт про підсумки роботи секції ВНТ за навчальний рік;</li>
                                <li>список складу секції ВНТ.</li>
                            </ul>
                            <p style="text-align: left;">
                            </p><p style="text-align: left;">4.17. Керівництво кафедри здійснює заходи щодо всебічного сприяння створенню належних умов для діяльності ВНТ (надає приміщення, меблі, оргтехніку, забезпечує телефонним зв’язком, постійним доступом до Інтернету, відводить місця для встановлення інформаційних стендів тощо).</p>
                            <p style="text-align: left;">
                            </p><ol style="text-align: left;">
                            <li>ОБОВ’ЯЗКИ ТА ПРАВА ЧЛЕНІВ ВОЄННО-НАУКОВОГО ТОВАРИСТВА КВП НАУ</li>
                        </ol>
                            <p style="text-align: left;">
                            </p><p style="text-align: left;">5.1. Члени ВНТ кафедри зобов’язані:</p>
                            <ul style="text-align: left;">
                                <li>проводити організаційні, наукові та освітні заходи;</li>
                                <li>популяризувати наукову діяльність серед студентської молоді, сприяти залученню осіб, які навчаються, до наукової роботи та інноваційної діяльності;</li>
                                <li>представляти інтереси студентів, курсантів та молодих учених перед адміністрацією кафедри та іншими організаціями з питань наукової роботи та розвитку академічної кар’єри;</li>
                                <li>сприяти підвищенню якості наукових досліджень;</li>
                                <li>сприяти обміну інформацією між молодими вченими та дослідниками;</li>
                                <li>сприяти розвитку міжвузівського співробітництва;</li>
                                <li>виконувати інші функції, передбачені Положенням про наукові товариства студентів (курсантів, студентів та курсантів), аспірантів, докторантів і молодих вчених Національного авіаційного університету, Законом України «Про вищу освіту», «Положенням про організацію наукової та науково-технічної діяльності в Збройних Силах України», затвердженого Наказом Міністерства оборони України від 27.07.2016 року № 385.</li>
                            </ul>
                            <p style="text-align: left;">
                            </p><p style="text-align: left;">5.2. Члени ВНТ мають право:</p>
                            <ul style="text-align: left;">
                                <li>вибирати та бути обраними в керівні органи ВНТ;</li>
                                <li>представляти свої роботи, ухвалені Радою ВНТ, в якості доповідей на семінарах і конференціях;</li>
                                <li>користуватися лабораторною базою КВП НАУ підготовки для виконання наукових дослідів у відповідності до затвердженої теми роботи;</li>
                                <li>брати участь у вільному обговоренні наукових робіт на планових засіданнях наукової секції і на воєнно-наукових конференціях;</li>
                                <li>представляти свої наукові роботи, схвалені секцією як доповіді, на семінарах і воєнно-наукових конференціях;</li>
                                <li>брати участь у конкурсах на кращу наукову роботу студентів та курсантів;</li>
                                <li>вирішального голосу на всіх зборах воєнно-наукового товариства кафедри;</li>
                                <li>брати участь в організації підготовки звітно-виборних зборів воєнно-наукового товариства кафедри.</li>
                            </ul>
                            <p style="text-align: left;">
                            </p><p style="text-align: left;">5.3. За невиконання обов’язків член товариства може бути виключеним з його рядів рішенням зборів товариства.</p>
                            <p style="text-align: left;">
                            </p><p style="text-align: left;">VІ. ФОРМИ ЗАОХОЧЕННЯ ЧЛЕНІВ ВОЄННО-НАУКОВОГО ТОВАРИСТВА КВП НАУ ЗА АКТИВНУ УЧАСТЬ У НАУКОВІЙ РОБОТІ</p>
                            <p style="text-align: left;">
                            </p><p style="text-align: left;">6.1. Студенти та курсанти – члени ВНТ, що проявили себе в проведенні наукової, дослідницької та винахідницької роботі можуть заохочуватися у відповідності з Дисциплінарним Статутом ЗС України, заохочувальними преміями з фондів КВП та НАУ.</p>
                            <p style="text-align: left;">
                            </p><p style="text-align: left;">6.2. Наукові роботи студентів та курсантів, що отримали заохочення за підсумками конкурсу, виставки, конференції рішенням начальника кафедри можуть бути зараховані як успішне виконання роботи за навчальним планом, а їх автори можуть бути звільнені від складання іспиту (заліку) з відповідного модулю.</p>
                            <p style="text-align: left;"><strong>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </strong></p>
                            <p style="text-align: left;">6.3. Наукова секція, яка зайняла перше місце за підсумками роботи за навчальний рік, наказом начальника кафедри нагороджується грамотою «За кращу організацію наукової роботи студентів та курсантів на кафедрі».</p>
