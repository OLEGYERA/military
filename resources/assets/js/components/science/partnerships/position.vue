                        </p><p style="text-align: center;"><strong>П О Л О Ж Е Н Н Я</strong></p>
                            <p style="text-align: center;">
                            </p><p style="text-align: center;">про воєнно-наукове товариство</p>
                            <p style="text-align: center;">кафедри військової підготовки</p>
                            <p style="text-align: center;">Національного авіаційного університету</p>
                            <p style="text-align: center;"><strong>&nbsp;</strong></p>
                            <p style="text-align: center;"><strong>&nbsp;</strong></p>
                            <p style="text-align: center;">І. ЗАГАЛЬНІ ПОЛОЖЕННЯ</p>
                            <p style="text-align: center;">
                            </p><p style="text-align: left;">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 1.1. Це Положення визначає особливості організації наукової роботи студентів, курсантів та молодих учених кафедри військової підготовки Національного авіаційного університету (далі – КВП НАУ).</p>
                            <p style="text-align: left;">
                            </p><p style="text-align: left;">1.2. Положення розроблено на підставі Законів України «Про вищу освіту», «Про наукову і науково-технічну діяльність» та «Положення про організацію наукової та науково-технічної діяльності в Збройних Силах України», затвердженого Наказом Міністерства оборони України від 27.07.2016&nbsp;року № 385.</p>
                            <p style="text-align: left;">1.3. Наукова і науково-технічна діяльність у Збройних Силах України – це інтелектуальна творча діяльність, спрямована на розвиток воєнної науки, підвищення її ефективності, використання наукових досягнень у військовому будівництві України.</p>
                            <p style="text-align: left;">
                            </p><p style="text-align: left;">1.4. Наукова і науково-технічна діяльність у Збройних Силах України здійснюється відповідно до Конституції України, Законів України «Про наукову і науково-технічну діяльність», «Про вищу освіту», «Про пріоритетні напрями розвитку науки і техніки», інших законів та нормативно-правових актів України, що регулюють відносини у сфері оборони, наказів і директив Міністра оборони України, начальника Генерального штабу – Головнокомандувача Збройних Сил України.</p>
                            <p style="text-align: left;">
                            </p><p style="text-align: left;">1.5. Основними суб’єктами наукової та науково-технічної діяльності у Збройних Силах є науково-дослідні установи, вищі військові навчальні заклади військові навчальні підрозділи вищих навчальних закладів України, наукові і науково-педагогічні працівники, які здійснюють наукову і науково-технічну діяльність з метою отримання наукових і науково-прикладних результатів.</p>
                            <p style="text-align: left;">
                            </p><p style="text-align: left;">На КВП НАУ суб’єктами наукової, науково-технічної та інноваційної діяльності є науково-педагогічні працівники, особи, які навчаються на кафедрі та університеті.</p>
                            <p style="text-align: left;">
                            </p><p style="text-align: left;">1.6. На КВП НАУ діє воєнно-наукове товариство (далі – ВНТ) студентів та курсантів, яке є частиною системи громадського самоврядування кафедри.</p>
                            <p style="text-align: left;">
                            </p><p style="text-align: left;">1.7. У своїй діяльності воєнно-наукове товариство КВП НАУ керується законодавством України, статутом НАУ та цим Положенням.</p>
                            <p style="text-align: center;">
                            </p><p style="text-align: center;">
                        </p><p style="text-align: center;">ІІ. МЕТА ТА ЗАВДАННЯ</p>
                            <p style="text-align: center;">ВОЄННО-НАУКОВОГО ТОВАРИСТВА КВП НАУ</p>
                            <p style="text-align: center;"><strong>&nbsp;</strong></p>
                            <p style="text-align: left;">2.1. Наукова та науково-технічна діяльність у військових навчальних підрозділах вищих навчальних закладів є невід’ємною складовою освітньої діяльності.</p>
                            <p style="text-align: left;">
                            </p><p style="text-align: left;">2.2. Наукова робота студентів та курсантів є окремим видом наукової та науково-технічної діяльності, яка проводиться під керівництвом науково-педагогічних працівників.</p>
                            <p style="text-align: left;">
                            </p><p style="text-align: left;">2.3. Метою наукової роботи студентів та курсантів КВП НАУ є підвищення методологічної підготовки, розвиток наукового мислення, набуття навичок дослідницької, винахідницької та раціоналізаторської роботи.</p>
                            <p style="text-align: left;">
                            </p><p style="text-align: left;">2.4. Залучення студентів та курсантів до наукової роботи дає змогу використовувати їх творчий&nbsp; потенціал для вирішення актуальних завдань кафедри та університету та впроваджувати у навчальний процес сучасні погляди на організацію повсякденної діяльності військ (сил) в мирний час, теорію і практику застосування та забезпечення військ (сил) у збройних конфліктах та в миротворчих операціях сучасності, зокрема із застосуванням існуючих в Збройних Силах України зразків озброєння і військової техніки, дослідження питань перспектив розвитку воєнного мистецтва, поглядів на війни майбутнього, вивчення актуальних питань євроатлантичної інтеграції України.</p>
                            <p style="text-align: left;">
                            </p><p style="text-align: left;">2.5. Воєнно-наукове товариство КВП НАУ забезпечує захист прав та інтересів осіб, які навчаються на кафедрі, зокрема в питаннях наукової діяльності, підтримки наукоємних ідей, інновацій та обміну знаннями.</p>
                            <p style="text-align: left;">
                            </p><p style="text-align: left;">2.6. Воєнно-наукове товариство кафедри діє на принципах:</p>
                            <ul style="text-align: left;">
                                <li>свободи наукової творчості;</li>
                                <li>добровільності, колегіальності, відкритості;</li>
                                <li>рівності права осіб, які навчаються, на участь у діяльності воєнно-наукового товариства.</li>
                            </ul>
                            <p style="text-align: left;"><strong>&nbsp;</strong></p>
                            <p style="text-align: left;">2.7. Діяльність ВНТ повинна бути тісно пов’язана з навчальним процесом і відповідати завданням воєнно-наукової, винахідницької та раціоналізаторської роботи&nbsp; на КВП НАУ.</p>
                            <p style="text-align: left;">
                            </p><p style="text-align: left;">2.8. Основними завданнями ВНТ є:</p>
                            <ul>
                                <li style="text-align: left;">поглиблення знань з військових і технічних наук, підвищення якості оволодіння навчальними дисциплінами;</li>
                                <li style="text-align: left;">оволодіння сучасною методикою і навичками проведення самостійних наукових досліджень та розробки військово-наукових тем з широким використанням математичного моделювання;</li>
                                <li style="text-align: left;">оволодіння формами організації і проведення наукових досліджень;</li>
                                <li style="text-align: left;">розвиток навичок патентно-ліцензійної, винахідницької і раціоналізаторської роботи щодо удосконалення озброєння і військової техніки та навчально-матеріальної бази кафедри;</li>
                                <li style="text-align: left;">популяризація військово-наукових і військово-технічних знань серед особового складу кафедри та університету;</li>
                                <li style="text-align: left;">виявлення з числа найбільш обдарованих і здібних студентів і курсантів резерву для подальшої підготовки в наукових підрозділах університету.</li>
                            </ul>
                            <p>&nbsp;</p>
                            <p style="text-align: center;">
                            </p><p style="text-align: center;">ІІІ. НАПРЯМИ ТА ОСНОВНІ ФОРМИ ДІЯЛЬНОСТІ</p>
                            <p style="text-align: center;">ВОЄННО-НАУКОВОГО ТОВАРИСТВА КВП НАУ</p>
                            <p style="text-align: left;">3.1. Наукова діяльність членів товариства повинна відповідати програмам навчання. Вона є його невід`ємним продовженням та повинна відповідати завданням навчального процесу та науково-дослідній роботі кафедри і є роботою, яка виконується поза навчальним часом. До переліку досліджень можуть бути включені також і ініціативні теми.</p>
                            <p style="text-align: left;">
                            </p><p style="text-align: left;">3.2. Основні форми воєнно-наукової роботи воєнно-наукового товариства:</p>
                            <ul style="text-align: left;">
                                <li>робота студентів та курсантів у секціях воєнно-наукового товариства;</li>
                                <li>виконання науково-дослідних робіт з військово-теоретичних питань;</li>
                                <li>аналіз, узагальнення та розповсюдження досвіду воєнних конфліктів, підготовки усіх видів Збройних Сил та родів військ, їх повсякденної діяльності;</li>
                                <li>патентно-ліцензійна, винахідницька і раціоналізаторська робота;</li>
                                <li>участь у конкурсах кафедри, університету, міжвузівських конкурсах наукових робіт Міністерства оборони України;</li>
                                <li>лекторська робота щодо розповсюдження знань у галузі військової науки і техніки;</li>
                                <li>участь в розвитку та удосконаленні навчальної матеріально-технічної бази КВП НАУ.</li>
                            </ul>
                            <p style="text-align: left;">
                            </p><p style="text-align: left;">3.3. Основними формами реалізації наукових робіт членів воєнно- наукового товариства КВП НАУ є:</p>
                            <ul style="text-align: left;">
                                <li>використання їх матеріалів у кваліфікаційних роботах;</li>
                                <li>використання отриманих результатів в програмах та алгоритмах для математичного моделювання;</li>
                                <li>включення матеріалів досліджень до наукових публікацій і праць (наукових статей, заявок на винахід (корисну модель), науково-дослідних робіт, командно-штабних навчань тощо;</li>
                                <li>використання матеріалів при створенні раціоналізаторських пропозицій та експонатів на виставках за результатами наукових досліджень;</li>
                                <li>впровадження наукових результатів у навчальний процес кафедри та практичну діяльність військ;</li>
                                <li>доповіді результатів наукової роботи на засіданнях секції, наукових семінарах і конференціях;</li>
                                <li>підготовка рефератів і наукових робіт, які подаються на конкурс кафедри, університету, конкурс наукових робіт у межах Міністерства оборони України і поза його межами;</li>
                                <li>підготовка анотацій оглядів і рефератів, які поділяються на наукову і науково-технічну літературу за джерелами інформації.</li>
                            </ul>
                            <p style="text-align: left;">
                            </p><p style="text-align: left;">3.4. З метою підбиття підсумків наукової роботи членів ВНТ за навчальний рік, популяризації кращих робіт і широкого обміну думками стосовно них, на кафедрі щорічно працюють секції Міжнародної науково-технічної конференції НАУ «Політ» за напрямком «Військова освіта та підготовка військових кадрів».</p>
                            <p style="text-align: left;">На МНТК «Політ» та інші наукові конференції для обговорення можуть виноситися найбільш вагомі роботи студентів та курсантів.</p>
                            <p style="text-align: left;">Теми доповідей на конференціях визначаються завчасно і обговорюються&nbsp; на секціях воєнно-наукового товариства.</p>
